#include "libft.h"

long	ft_atol(const char *str)
{
	long	nb;
	int		i;
	int		signal;

	nb = 0;
	i = 0;
	signal = 1;
	while (str[i] && (str[i] == 32 || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signal = -1;
		i++;
	}
	while (str[i] && ft_isdigit(str[i]))
		nb = nb * 10 + str[i++] - 48;
	return (nb * signal);
}
